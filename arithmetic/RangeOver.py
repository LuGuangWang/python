# 我们可以用2*1的小矩形横着或者竖着去覆盖更大的矩形。
# 请问用n个2*1的小矩形无重叠地覆盖一个2*n的大矩形，总共有多少种方法？

#f[n] = f[n-1] + f[n-2] // 当小矩形竖着放时，大矩形还剩下 n-1 的规模，当小矩形横着放时，大矩形还剩下 n-2 的规模


def sum(n):
    s = 0
    if n == 0:
        s = 0
    elif n == 1:
        s = n
    elif n == 2:
        s = n
    else:
        s = sum(n-1) + sum(n-2)

    return s

